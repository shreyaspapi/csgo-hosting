import * as React from "react"

import { cn } from "@/lib/utils"

function Card({
  className,
  size = "default",
  ...props
}: React.ComponentProps<"div"> & { size?: "default" | "sm" }) {
  return (
    <div
      data-slot="card"
      data-size={size}
      className={cn(
        "group/card vgui-panel flex flex-col gap-0 overflow-hidden text-sm text-foreground",
        className
      )}
      {...props}
    />
  )
}

